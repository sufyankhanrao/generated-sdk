    def __init__(self, http_client_instance=None,
                 override_http_client_configuration=False, http_call_back=None,
                 timeout=60, max_retries=0, backoff_factor=2,
                 retry_statuses=None, retry_methods=None, proxy_settings=None,
                 environment=Environment.QA, api_key_credentials=None,
                 session_key_credentials=None, txn_session_key_credentials=None,
                 username_credentials=None, password_credentials=None,
                 config=None):
        """Initialize a new instance of PayrixClient."""
        self.config = config or Configuration(
            http_client_instance=http_client_instance,
            override_http_client_configuration=override_http_client_configuration,
            http_call_back=http_call_back, timeout=timeout,
            max_retries=max_retries, backoff_factor=backoff_factor,
            retry_statuses=retry_statuses, retry_methods=retry_methods,
            proxy_settings=proxy_settings, environment=environment,
            api_key_credentials=api_key_credentials,
            session_key_credentials=session_key_credentials,
            txn_session_key_credentials=txn_session_key_credentials,
            username_credentials=username_credentials,
            password_credentials=password_credentials)

        self.global_configuration = GlobalConfiguration(self.config)\
            .global_errors(BaseController.global_errors())\
            .base_uri_executor(self.config.get_base_uri)\
            .user_agent(BaseController.user_agent(),
                BaseController.user_agent_parameters())

        self.auth_managers = {
            "apiKey": ApiKey(self.config.api_key_credentials),
            "sessionKey": SessionKey(self.config.session_key_credentials),
            "txnSessionKey": TxnSessionKey(
                self.config.txn_session_key_credentials),
            "username": Username(self.config.username_credentials),
            "password": Password(self.config.password_credentials),
        }
        self.global_configuration =\
            self.global_configuration.auth_managers(self.auth_managers)

    @classmethod
    def from_environment(cls, dotenv_path=None, **overrides):
        """Create a client instance using environment variables.

        Returns:
            PayrixClient instance.

        """
        return cls(config=Configuration
            .from_environment(dotenv_path=dotenv_path, **overrides))
